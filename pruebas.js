const isPalindrome = (text) => {
  if (typeof text == "string") {
    const textoOriginal = arrOfLetters(text).join("").toLowerCase();
    const textoRevertido = stringReverser(arrOfLetters(text).join("").toLowerCase());
    return textoOriginal === textoRevertido;
  } else return "isPalindrome only works with strings";
};

