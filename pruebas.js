const stringReverser = (text = "") => {
  //Separa las letras y las coloca en un array
  const textToArr = splitter(text);
  let output = "";
  textToArr.map((letter) => {
    output = [letter, ...output];
  });
  return output.join("");
};
console.log("b:", stringReverser(saludo));

//? con recursividad
const stringReverserR = (text = "") => {
  const textToArr = splitter(text);
  text = "";
  for (const letter of textToArr) {
    text = letter + text;
  }
  return text;
};

